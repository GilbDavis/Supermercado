    public void CargarArticulo(){
        DefaultTableModel modelo = (DefaultTableModel) ProductoTablatbl.getModel();
        modelo.setRowCount(0);
        try{
            PreparedStatement stmt = ClasesInternas.ConexionAdmin.getConexion().
                prepareStatement("SELECT Producto.idproducto, Producto.nombre,"
                        + " Producto.precio, Producto.stock, Categoria.nombre "
                        + "FROM Producto\n" + "INNER JOIN Categoria ON "
                        + "Producto.idcategoria = Categoria.idcategoria;");
            res = stmt.executeQuery();
            while(res.next()){
             Vector v = new Vector();
                v.add(res.getInt(1));
                v.add(res.getString(2));
                v.add(res.getFloat(3));
                v.add(res.getInt(4));
                v.add(res.getString(5));
                modelo.addRow(v);
                ProductoTablatbl.setModel(modelo);
            }
        }catch(SQLException e){
            e.getMessage();
        }
    }
    
