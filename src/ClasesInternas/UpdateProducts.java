    private void ProductoNuevobtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProductoNuevobtnActionPerformed
        // TODO add your handling code here:
        if(ProductoNombretxt.getText().isEmpty() ||
                ProductoPreciotxt.getText().isEmpty() || 
                ProductoStocktxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Llene todos los espacios",
                    "Informacion", JOptionPane.INFORMATION_MESSAGE);
            ProductoNombretxt.setText("");
            ProductoPreciotxt.setText("");
            ProductoStocktxt.setText("");
            ProductoNombretxt.requestFocus();
            ProductoPreciotxt.requestFocus();
            ProductoStocktxt.requestFocus();
        }else{
            try{
                PreparedStatement stmt = ClasesInternas.ConexionAdmin.
                        getConexion().prepareStatement("INSERT INTO Producto"
                                + " (idcategoria, nombre, precio, stock) VALUES "
                                + "(?, ?, ?, ?)");
                stmt.setInt(1, ProductoCategoriacmbx.getSelectedIndex() + 1);
                stmt.setString(2, ProductoNombretxt.getText());
                stmt.setString(3, ProductoPreciotxt.getText());
                stmt.setString(4, ProductoStocktxt.getText());
                ResultSet res = stmt.executeQuery();
                stmt.close();
                res.close();
            }catch(SQLException e){
                e.getMessage();
            }
        }
        CargarArticulo();
    }//GEN-LAST:event_ProductoNuevobtnActionPerformed

