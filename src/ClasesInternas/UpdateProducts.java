    private void ProductoActualizarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProductoActualizarbtnActionPerformed
        // TODO add your handling code here:
        int opc = JOptionPane.showConfirmDialog(this, "¿ESTA SEGURO QUE DESEA "
                + "MODIFICAR EL PRODUCTO?", "INFO", JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if(opc == JOptionPane.YES_OPTION){
            try{
                
                PreparedStatement stmt = ClasesInternas.ConexionAdmin.
                        getConexion().prepareStatement("UPDATE Producto SET "
                                + "idcategoria = ?, nombre = ?, precio = ?, "
                                + "stock = ? WHERE idproducto = ?");
                stmt.setInt(1, ProductoCategoriacmbx.getSelectedIndex() + 1);
                stmt.setString(2, ProductoNombretxt.getText());
                stmt.setString(3, ProductoPreciotxt.getText());
                stmt.setString(4, ProductoStocktxt.getText());
                stmt.setString(5, ProductoIdtxt.getText());
                ResultSet res = stmt.executeQuery();
                JOptionPane.showMessageDialog(this, "Datos Actualizados!", 
                        "INFO", JOptionPane.INFORMATION_MESSAGE);
                stmt.close();
                res.close();
            }catch(SQLException e){
                e.getMessage();
            }
        }
        CargarArticulo();
    }//GEN-LAST:event_ProductoActualizarbtnActionPerformed

