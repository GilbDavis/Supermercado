    private void CashierLoginBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CashierLoginBTNActionPerformed
        // TODO add your handling code here:
        //Variables que reciben los datos de los campos de texto
        String mUser = CashierIdTXT.getText();
        String mPassword = CashierPassJPF.getText();
        
        /*
        Este fragmento de codigo se encarga de conectarse y autenticar si el
        usuario y contraseña existen en la base de datos y que los datos sean
        los correctos para poder entrar al menu de cajero
        */
        AutenticacionCashier aut = new AutenticacionCashier();
        //Llama a la clase ConexionCashier para brindar una facil y rapida conexion
        ClasesInternas.ConexionCashier.getConexion();
        //Se valida si los datos son autenticos utilizando la clase
        //AutenticacionCashier
        if(aut.Autenticacion(mUser, mPassword) == true){
            CashierMenu cashiermenu = new CashierMenu();
            cashiermenu.setVisible(true);
            JOptionPane.showMessageDialog(null, "Bienvenido " + mUser + "!",
                    "Felicidades", JOptionPane.INFORMATION_MESSAGE);
            CashierIdTXT.setText("");
            CashierPassJPF.setText("");
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "USUARIO Y CONTRASEÑA INCORRECTO",
                    "Error de Autenticacion", JOptionPane.ERROR_MESSAGE);
            CashierIdTXT.setText("");
            CashierPassJPF.setText("");
        }
        
    }//GEN-LAST:event_CashierLoginBTNActionPerformed

